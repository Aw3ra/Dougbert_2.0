import os
import json

# ---------------------------------------------------------------------------------#
# Function too return data from a json file
# Inputs:  Name - the name of the json file
# Outputs: data - the data from the json file
# ---------------------------------------------------------------------------------#
def find_json_file(json_name):
    # -----------------------------------------------------------------------------#
    # For each file in the current directory
    # -----------------------------------------------------------------------------#
    for root, dirs, files in os.walk(os.getcwd()):
        # -------------------------------------------------------------------------#
        # If the file is the json file
        # -------------------------------------------------------------------------#
        if json_name in files:
            # ---------------------------------------------------------------------#
            # Open the file and return the data
            # ---------------------------------------------------------------------#
            with open(os.path.join(root, json_name), "r") as f:
                # -----------------------------------------------------------------#
                # Read the data from the file
                # -----------------------------------------------------------------#
                data = json.load(f)
            # ---------------------------------------------------------------------#
            # Return the data
            # ---------------------------------------------------------------------#
            return data
    # -----------------------------------------------------------------------------#
    # If the file is not found, return an error
    # -----------------------------------------------------------------------------#
    return "Error: JSON file not found."
